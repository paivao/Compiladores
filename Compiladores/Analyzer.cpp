Analyzer::Analyzer(LexicalAnalyzer& analizer, Semantics& semantics)
	: lexical(analizer), semantics(semantics), finalAction(FINAL_ACTION)
{
	//actionTable.reserve(ACTION_TABLE_SIZE);
	InitializeActionTable();
	InitializeRuleTable();
	syntaxStack.reserve(SYNTAX_STACK_SIZE);
}


Analyzer::~Analyzer()
{
}

void Analyzer::Parse(const std::string & codeFile)
{
	lexical.OpenSourceFile(codeFile);
	unsigned int q = 0;
	Token a;
	syntaxStack.push_back(0);
	try {
		a = lexical.NextToken();
	}
	catch (std::exception& ex) {
		std::cout << "Lexical error: " << std::endl << ex.what() << std::endl;
	}
	int p;
	do {
		try {
			p = NextAction(q, a);
		}
		catch (std::out_of_range&) {
			SyntaxError(lexical.GetCurrentLine());
		}
		if (IsShift(p)) {
			syntaxStack.push_back(p);
			try {
				a = lexical.NextToken();
				std::cout << "Next token: " << TokenIdx(a) << std::endl;
			}
			catch (std::exception& ex) {
				std::cout << "Lexical error: " << std::endl << ex.what() << std::endl;
			}
		}
		else if (IsReduction(p)) {
			unsigned int r = Rule(p);
			syntaxStack.erase(syntaxStack.end() - ruleTable[r].first, syntaxStack.end());
			syntaxStack.push_back(NextAction(syntaxStack.back(), ruleTable[r].second));
			semantics(static_cast<Rules>(r), lexical.GetSecondaryToken());
		}
		q = syntaxStack.back();
	} while (q != finalAction);
}

